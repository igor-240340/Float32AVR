            ; ВЫЧИСЛЕНИЕ ЭКСПОНЕНТЫ ПРОИЗВЕДЕНИЯ.
            ;
            ; ПОСКОЛЬКУ ЭКСПОНЕНТЫ ПРЕДСТАВЛЕНЫ В КОДЕ СО СМЕЩЕНИЕМ, ТО
            ; ИХ ЗНАЧЕНИЯ ВСЕГДА ЯВЛЯЮТСЯ ПОЛОЖИТЕЛЬНЫМИ ЧИСЛАМИ В ДИАПАЗОНЕ [1,254].
