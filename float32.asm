            COM MANTB2                  ; ЗАНУЛЯЮЩИЙ РАЗРЯД ИСТИННОГО ДОП. КОДА.
            LDI R16,1                   ;
            ADD R7,R16                  ;
            CLR R16                     ;
            ADC MANTB0,R16              ;
            ADC MANTB1,R16              ;
            ADC MANTB2,R16              ;

            LDI SREGACC,0b00000010      ; МАСКА ДЛЯ Z-ФЛАГА.
            ADD R6,R7                   ; СКЛАДЫВАЕМ RGS-РЕГИСТРЫ.
