            ROR MANTP5                  ; НЕТ, ДЕЛИМ АККУМУЛЯТОР НА 2.
            ROR MANTP4                  ;
            ROR MANTP3                  ;
            ROR MANTP2                  ;
            ROR MANTP1                  ;
            ROR MANTP0                  ;            

