            LDI R16,0b10000000          ; ЗА ПРИЗНАК БЕРЕМ НУЛЕВОЙ РЕЗУЛЬТАТ И ЗНАК ПОЛУЧЕННОЙ РАЗНОСТИ.
            ADD MANTP2,R16              ; 
