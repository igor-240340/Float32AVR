            RJMP HALFWAY                ; НЕТ, ПРОВЕРЯЕМ СЛЕДУЮЩИЙ ВАРИАНТ.
            LDI R16,1                   ; ДА, МЛАДШАЯ ЧАСТЬ БОЛЬШЕ ULP/2. ОКРУГЛЯЕМ В БОЛЬШУЮ СТОРОНУ.
