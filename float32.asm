            ; ОКРУГЛЕНИЕ.
            ;
            ; ПОСЛЕ СДВИГА R-БИТА ПРОИСХОДИТ ПРОВЕРКА C- И Z-БИТОВ В РЕГИСТРЕ СТАТУСА.
