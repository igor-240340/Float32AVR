            ;
            ; ОКРУГЛЕНИЕ К БЛИЖАЙШЕМУ.
            ;
            ; ВОЗМОЖНЫЕ СОЧЕТАНИЯ БИТОВ RS. ДЛЯ КРАТКОСТИ GUARD-БИТ ЗДЕСЬ НЕ УЧИТЫВАЕТСЯ, ИЛЛЮСТРИРУЕТСЯ САМА ИДЕЯ ОКРУГЛЕНИЯ.
