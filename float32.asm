            ADC R9,R13                  ;
            ADC R10,R14                 ;
                                        ; ПЕРЕПОЛНЕНИЕ ЕСТЬ?
            BRCC ROUNDSUM               ; НЕТ, ПЕРЕХОДИМ К ОКРУГЛЕНИЮ.
            ROR R10                     ; ДА, НОРМАЛИЗУЕМ МАНТИССУ ВПРАВО.
            ROR R9                      ;
            ROR R8                      ;
            ROR R6                      ;
