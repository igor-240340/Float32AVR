            LDI R16,0                   ; ЭТО ЭКВИВАЛЕНТНО ПРИБАВЛЕНИЮ К МЛАДШЕЙ ЧАСТИ ВЕЛИЧИНЫ МЕНЬШЕ ULP/2,
